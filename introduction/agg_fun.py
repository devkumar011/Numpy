##Aggregation means summarizing or combining data ? in other words, taking a large dataset 
# and producing a single meaningful value from it (like sum, mean, min, max, etc.).




import numpy as np

arr = np.array([10,20,30,40,50])


print(np.sum(arr))
print(np.mean(arr))
print(np.min(arr))
print(np.max(arr))
print(np.std(arr))
print(np.var(arr))

